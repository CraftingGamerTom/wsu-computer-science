
public class Pair {

    private int xCord;
    private int yCord;
    public Pair(int x, int y) {
        setxCord(x);
        setyCord(y);
    }
    /**
     * @return the yCord
     */
    public int getyCord() {
        return yCord;
    }
    /**
     * @param yCord the yCord to set
     */
    public void setyCord(int yCord) {
        this.yCord = yCord;
    }
    /**
     * @return the xCord
     */
    public int getxCord() {
        return xCord;
    }
    /**
     * @param xCord the xCord to set
     */
    public void setxCord(int xCord) {
        this.xCord = xCord;
    }
    

    
}
